def extract_parts(title):
    found = []
    tl = title.lower()
    for kw in PART_KEYWORDS:
        if kw.lower() in tl:
            if any(
                bl.lower() in kw.lower() or kw.lower() in bl.lower()
                for bl in PART_BLACKLIST
            ):
                continue
            if not any(
                kw.lower() in f.lower() or f.lower() in kw.lower()
                for f in found
            ):
                found.append(kw)
    return found
